class TextButtonWithIcon extends StatelessWidget {
  final String text;
  final VoidCallback? onPressed;
  final IconData icon;
  final Color? color;

  const TextButtonWithIcon({
    super.key,
    required this.text,
    this.onPressed,
    required this.icon,
    this.color,
  });

  @override
  Widget build(BuildContext context) {
    return TextButton.icon(
      onPressed: onPressed,
      icon: Icon(icon, color: color ?? AppColors.primary),
      label: Text(
        text,
        style: TextStyle(color: color ?? AppColors.primary),
      ),
    );
  }
}
